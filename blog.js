// Initialize blog functionality
document.addEventListener('DOMContentLoaded', function() {
    const blogGrid = document.getElementById('blogGrid');
    const blogModal = document.getElementById('blogModal');
    const modalBody = document.getElementById('modalBody');
    const closeModal = document.getElementById('closeModal');

    // Load blog posts
    function loadBlogPosts() {
        blogGrid.innerHTML = '';
        blogPosts.forEach(post => {
            const postElement = createBlogPostElement(post);
            blogGrid.appendChild(postElement);
        });
    }

    // Create blog post element
    function createBlogPostElement(post) {
        const postDiv = document.createElement('div');
        postDiv.className = 'blog-post-card';
        postDiv.setAttribute('data-post-id', post.id);
        
        postDiv.innerHTML = `
            <div class="post-image">
                <img src="${post.image}" alt="${post.title}">
            </div>
            <div class="post-content">
                <div class="post-category">${post.category}</div>
                <h3 class="post-title">${post.title}</h3>
                <p class="post-excerpt">${post.excerpt}</p>
                <div class="post-meta">
                    <span class="post-date">${post.date}</span>
                    <span class="read-time">${post.readTime}</span>
                </div>
            </div>
        `;

        // Add click event
        postDiv.addEventListener('click', () => openBlogPost(post));
        
        return postDiv;
    }

    // Open blog post modal
    function openBlogPost(post) {
        modalBody.innerHTML = post.content;
        blogModal.style.display = 'flex';
        document.body.style.overflow = 'hidden';
        
        // Add fade-in animation
        setTimeout(() => {
            blogModal.classList.add('active');
        }, 10);
    }

    // Close modal
    function closeBlogModal() {
        blogModal.classList.remove('active');
        setTimeout(() => {
            blogModal.style.display = 'none';
            document.body.style.overflow = 'auto';
        }, 300);
    }

    // Event listeners
    closeModal.addEventListener('click', closeBlogModal);
    blogModal.addEventListener('click', (e) => {
        if (e.target === blogModal) {
            closeBlogModal();
        }
    });

    // Close modal with Escape key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && blogModal.style.display === 'flex') {
            closeBlogModal();
        }
    });

    // Load posts
    loadBlogPosts();
}); 